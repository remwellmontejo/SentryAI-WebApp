import mongoose from "mongoose";

const UserSchema = new mongoose.Schema({
    firstName: {
        type: String,
        default: ''
    },
    lastName: {
        type: String,
        default: ''
    },
    username: {
        type: String,
        required: true,
        unique: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    status: {
        type: String,
        enum: ['active', 'inactive'],
        default: 'inactive'
    },
    role: {
        type: String,
        enum: ['Admin', 'Employee'],
        default: 'Employee'
    }
}, { timestamps: true });

const UserModel = mongoose.model('User', UserSchema);
export default UserModel;